export type AbilitySubject =
    | 'math'
    | 'physics'
    | 'chemistry'
    | 'biology'
    | 'english'
    | 'chinese'
    | 'history'
    | 'geography'
    | 'politics'
    | 'other';

export interface AbilityTagDefinition {
    code: string;
    name: string;
    subject: AbilitySubject;
    description: string;
    order: number;
}

function defineAbilityTags(subject: AbilitySubject, entries: Array<[name: string, description: string]>): AbilityTagDefinition[] {
    return entries.map(([name, description], index) => ({
        code: `${subject}.ability.${String(index + 1).padStart(2, '0')}`,
        name,
        subject,
        description,
        order: index + 1,
    }));
}

